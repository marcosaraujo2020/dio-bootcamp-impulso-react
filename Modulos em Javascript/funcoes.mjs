function somaNumeros(x, y){
    return `A soma de ${x} + ${y} = ${x + y}`;
}

function subtracaoNumeros(m, n){
    return `A subtração de ${m} - ${n} = ${m -n}`;
}

function produtoNumeros(z, w){
    return `O produto de ${z} x ${w} = ${z *w}`;
}

function divisaoNumeros(h, v){
    return `A divisão de ${h} / ${v} = ${h / v}`;
}

export{somaNumeros, subtracaoNumeros, produtoNumeros, divisaoNumeros};